package com.obss.jss.day1;

public class EscapeSequences {

    public static void main(String[] args) {

        // new line
        System.out.println("Hello\nWorld");
        System.out.println("**********");

        // tab
        System.out.println("Hello\tWorld");
        System.out.println("**********");

        // print backslash in a string
        System.out.println("\\");
        System.out.println("**********");

        // print double quotes in a string
        System.out.println("\"yasin\"");
    }
}
